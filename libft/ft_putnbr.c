#include "libft.h"

void	ft_putnbr(int nb)
{
	int a;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		a = nb % 10 + '0';
		write(1, &a, 1);
	}
	else if (nb >= 0)
	{
		a = nb + '0';
		write(1, &a, 1);
	}
}
