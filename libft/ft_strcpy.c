#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int i;
	int n;

	n = ft_strlen(src);
	i = 0;
	while (i < n)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
